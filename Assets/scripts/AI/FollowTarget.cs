using UnityEngine;
using System.Collections;

public class FollowTarget : MovingRigidBody
{
  public Transform m_target;

  // ------------------------------------------------------------------------------------------------------------------------------------------
  
  void Start ()
  {
  
  }
  
  // ------------------------------------------------------------------------------------------------------------------------------------------
  
  void FixedUpdate ()
  {
    Move (m_target.position);
    
    Stabilize (rigidbody.velocity.normalized);
    
    Lean ();
  }

  // ------------------------------------------------------------------------------------------------------------------------------------------
  
}
